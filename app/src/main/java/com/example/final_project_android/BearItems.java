package com.example.final_project_android;
public class BearItems {
    int image;
    int width;
    int height;


    public BearItems(int image, int width, int height) {
        this.image = image;
        this.width = width;
        this.height = height;
    }

    public int getImage() {
        return image;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }


}
